/**
 * UI strings for the per-range page (`UniversalRangePage`), structurally parallel
 * to `lib/number-page-translations.ts` and `lib/header-translations.ts`.
 *
 * Fields documented with "Pattern: {rangeStart}" etc. are filled in via `fillTemplate()`
 * before rendering. Extra computed tokens beyond {rangeStart}/{rangeEnd} (mirroring how
 * number-page-translations.ts used {product4} beyond {number}) are:
 *   {totalOps}        - rangeNumbers.length * 10
 *   {tripleSum}        - rangeStart * 3
 *   {evenNumbersList}  - rangeNumbers.filter(n => n % 2 === 0).join(', ')
 *   {n}                - the current number in a per-number map() iteration (range table
 *                         cards, print popup boxes, and individual number links). This is
 *                         intentionally NOT called {number} to avoid confusion with
 *                         NumberPage's {number} token, which represents something different.
 *
 * The print popup's literal `body`/`<body>`/`</body>` HTML/CSS keywords are hardcoded in
 * UniversalRangePage's print handler, never sourced from translation strings (this is the
 * fix for the old uk source's "Рахунок" mistranslation bug).
 */
export interface RangePageTranslations {
  difficultyLabels: { beginner: string; intermediate: string; advanced: string }
  difficultyDescriptions: { beginner: string; intermediate: string; advanced: string }
  /** Badge text wrapping the difficulty label. Pattern: {label}. Most locales are just
   *  '{label}'; fi prefixes with 'Taso: {label}' ("Level: {label}"). */
  difficultyBadgeLabel: string

  /** Static text under the "{rangeStart} - {rangeEnd}" gradient heading, e.g. 'Times Tables'. */
  heroTitleSuffix: string
  statNumbersLabel: string
  statOperationsLabel: string
  statPracticeValue: string
  statPracticeLabel: string
  /** Pattern: {rangeStart}, {rangeEnd} */
  heroDescription: string
  ctaPractice: string
  ctaGames: string

  /** Pattern: {rangeStart}, {rangeEnd} */
  learningContextHeading: string
  /** Pattern: {rangeStart}, {rangeEnd} */
  learningContextIntro: string
  /** Pattern: {rangeStart}, {rangeEnd}, {totalOps} */
  learningContextCallout: string

  /** Pattern: {rangeStart}, {rangeEnd}. Base heading reused for: the section h2 and print
   *  h1 (both append " - " + rangeTableSuffix), and the print window's <title> (no suffix). */
  rangeTableHeading: string
  rangeTableSuffix: string
  /** Pattern: {rangeStart}, {rangeEnd} */
  rangeTableIntro: string
  printButtonLabel: string
  printButtonTitle: string
  /** Pattern: {rangeStart}, {rangeEnd} */
  printButtonAriaLabel: string
  /** Pattern: {n} */
  numberTableTitle: string

  linksHeading: string
  linksIntro: string
  /** Pattern: {n} */
  linkCardTitle: string
  /** Pattern: {n} */
  linkCardDescription: string

  progressionHeading: string
  /** Appended after difficultyDescriptions[level] when rangeStart === 1 */
  progressionFirstRangeNote: string
  /** Appended after difficultyDescriptions[level] otherwise */
  progressionLaterRangeNote: string
  cognitiveHeading: string
  cognitiveBody: string
  /** Shown only when nextRangeUrl is defined */
  nextStepHeading: string
  nextStepBody: string

  /** Pattern: {rangeStart}, {rangeEnd} */
  patternsHeading: string
  patternsIntro: string
  commutativeCardTitle: string
  /** Pattern: {rangeStart}, {rangeEnd} */
  commutativeCardBody: string
  commutativeCardExample: string
  additionCardTitle: string
  /** Pattern: {rangeStart}, {tripleSum} */
  additionCardBody: string
  additionCardExample: string
  evenCardTitle: string
  /** Pattern: {evenNumbersList} */
  evenCardBodyWithEvens: string
  evenCardBodyNoEvens: string
  evenCardExample: string
  lastDigitCardTitle: string
  lastDigitCardBody: string
  lastDigitCardExample: string

  /** Pattern: {rangeStart}, {rangeEnd} */
  studyHeading: string
  studyStep1Title: string
  studyStep1Body: string
  studyStep1TipLabel: string
  studyStep1TipBody: string
  studyStep2Title: string
  studyStep2Body: string
  studyStep2Bullet1Label: string
  /** Pattern: {rangeStart} (used twice) */
  studyStep2Bullet1Body: string
  studyStep2Bullet2Label: string
  studyStep2Bullet2Body: string
  studyStep2Bullet3Label: string
  studyStep2Bullet3Body: string
  studyStep3Title: string
  studyStep3Body: string
  studyStep3Card1Title: string
  studyStep3Card1Body: string
  studyStep3Card2Title: string
  studyStep3Card2Body: string
  studyStep3Card3Title: string
  studyStep3Card3Body: string
  studyStep3Card4Title: string
  studyStep3Card4Body: string

  navHeadingContinue: string
  navHeadingComplete: string
  /** Pattern: {rangeStart}, {rangeEnd} */
  navContinueText: string
  navPrevLabel: string
  navNextLabel: string
  navCompleteText: string
  navFooterNote: string
}

